# object_detector/config.py
"""
检测配置模块：集中管理所有可调参数，包括颜色阈值、形态学、面积、形状及大小预设。
修改此文件可永久调整检测行为，无需改动其他模块。
"""

class DetectionConfig:
    # ==================== 红色 HSV 范围 ====================
    # 红色在 HSV 中分布于 0~10 和 170~180 两段，需联合使用
    RED_LOWER1 = (0, 100, 45)
    RED_UPPER1 = (10, 255, 255)
    RED_LOWER2 = (170, 100, 45)
    RED_UPPER2 = (180, 255, 255)

    # ==================== 蓝色 HSV 范围 ====================
    BLUE_LOWER = (100, 130, 70)
    BLUE_UPPER = (130, 255, 255)

    # ==================== 白色 HSV 范围（用于合并反光区域） ====================
    # 白色特征：低饱和度（S < 35）、高亮度（V > 190），色相 H 无关紧要
    WHITE_LOWER = (0, 0, 190)
    WHITE_UPPER = (180, 35, 255)

    # 是否将白色区域合并到红/蓝掩膜中（反光条/高亮区域）
    # 蓝色通常无反光条，仅红色需要合并白色
    MERGE_WHITE_TO_RED = True
    MERGE_WHITE_TO_BLUE = False

    # ==================== 形态学参数 ====================
    # 核大小 = max(MIN_KERNEL_SIZE, 图像宽度 / MORPH_KERNEL_DIVIDER)
    MORPH_KERNEL_DIVIDER = 85       # 数值越小核越大，去噪更强
    MIN_KERNEL_SIZE = 3

    # ==================== 面积筛选 ====================
    # 物体面积占画面比例的下限与上限（超出范围则忽略）
    MIN_AREA_RATIO = 0.005
    MAX_AREA_RATIO = 0.8

    # ==================== 形状筛选 ====================
    TARGET_ASPECT = 25.0 / 18.0     # 目标宽高比（宽/高 ≈ 1.389）
    ASPECT_TOL = 0.7                # 宽高比允许偏差（越大越宽松）
    EXTENT_THRESH = 0.45            # 矩形度下限（0~1，越小越包容不规则形状）

    # ==================== 物体大小预设 ====================
    # 用于快速切换面积区间，可在调参窗口中通过 Size 滑块直接调用
    SIZE_PRESETS = {
        "small":   (0.005, 0.15),   # 小物体：占比 0.5% ~ 15%
        "medium":  (0.02,  0.50),   # 中物体：占比 2%   ~ 50%
        "large":   (0.10,  0.85),   # 大物体：占比 10%  ~ 85%
    }
    # 当前激活的大小预设（程序启动时的默认值）
    ACTIVE_SIZE_PRESET = "medium"